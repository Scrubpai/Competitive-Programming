ll pwmd(ll b, ll exp) {
    ll ret = 1;
    while (exp > 0) {
        if (exp % 2 == 1) ret = (ret * b) % MOD;
        exp /= 2;
        b = (b * b) % MOD;
    }
    return ret;
}

int D, I, R, dp[1005][1005]; string a, b;

void solve(int tc) {

    cin >> D >> I >> R >> a >> b; a = " " + a; b = " " + b; int n = a.size(), m = b.size();
    for (int i = 1; i <= m; i++) dp[0][i] = i * I;
    for (int i = 1; i <= n; i++) dp[i][0] = i * D;
    for (int i = 1; i <= n; i++) {
        for (int j = 1; j <= m; j++) {
            if (a[i] == b[j]) dp[i][j] = dp[i-1][j-1];
            else dp[i][j] = min({dp[i-1][j] + D, dp[i][j-1] + I, dp[i-1][j-1] + R});
        }
    }
    cout << dp[n][m] << nl;

}

